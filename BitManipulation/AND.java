package BitManipulation;

public class AND {
    public static void main(String args[]){
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6); 
        System.out.println(~7);
        System.out.println(6<<3);
        System.out.println(6>>3);
    }
    
}
