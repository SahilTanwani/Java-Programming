package JavaBasics;

public class datatypes {
    public static void main(String args[]){
        byte a = 10;
        System.out.println(a);
        char ch = '@';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        int c = 25;
        System.out.println(c);
        double d = 7.8;
        System.out.println(d);
        float e = 3.14f;
        System.out.println(e);
    }
    
}
